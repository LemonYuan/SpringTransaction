package com.hand.SpringTransaction.entity;

public class Course {
  private int id;
  private int sid;
  private String course;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}
public String getCourse() {
	return course;
}
public void setCourse(String course) {
	this.course = course;
}
  
}
